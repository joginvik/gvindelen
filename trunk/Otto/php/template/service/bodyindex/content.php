			Как правильно определить свой размер?<br>
      Если Вы не знаете свой размер или Ваша фигура изменилась, то необходимо снять мерки<br>
      Производите замеры в нижнем белье. Не напрягайтесь. Прижимайте измерительную ленту вплотную к телу, потому что все размеры, указанные  в таблицах, представляют собой размеры фигуры. Попросите кого-нибудь помочь Вам &ndash; так Вы добьетесь большей точности. Все размеры указаны в сантиметрах.
    </div>
    
